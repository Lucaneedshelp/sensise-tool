const state = {
  search: '',
  category: 'Alle',
  cart: JSON.parse(localStorage.getItem('sensise-project-calculator-cart') || '[]')
};

const euro = new Intl.NumberFormat('de-DE', { style: 'currency', currency: 'EUR' });
const amount = new Intl.NumberFormat('de-DE', { maximumFractionDigits: 1 });

const els = {
  catalogCount: document.getElementById('catalog-count'),
  lineCount: document.getElementById('line-count'),
  grandTotal: document.getElementById('grand-total'),
  hardwareTotal: document.getElementById('hardware-total'),
  softwareTotal: document.getElementById('software-total'),
  serviceTotal: document.getElementById('service-total'),
  weightTotal: document.getElementById('weight-total'),
  resultCount: document.getElementById('result-count'),
  catalogBody: document.getElementById('catalog-body'),
  cartBody: document.getElementById('cart-body'),
  emptyCart: document.getElementById('empty-cart'),
  search: document.getElementById('search'),
  categoryFilter: document.getElementById('category-filter'),
  vatMode: document.getElementById('vat-mode'),
  projectName: document.getElementById('project-name'),
  customerName: document.getElementById('customer-name')
};

function isRecurring(product) {
  return product.unit.toLowerCase().includes('monat');
}

function saveCart() {
  localStorage.setItem('sensise-project-calculator-cart', JSON.stringify(state.cart));
}

function getDisplayTotal(value) {
  return euro.format(els.vatMode.value === 'gross' ? value * 1.19 : value);
}

function getLineTotal(product, line) {
  const months = isRecurring(product) ? Math.max(Number(line.months) || 1, 1) : 1;
  const discount = Math.min(Math.max(Number(line.discount) || 0, 0), 100);
  return product.price * (Number(line.qty) || 0) * months * (1 - discount / 100);
}

function getProduct(productId) {
  return products.find(product => product.id === productId);
}

function filteredProducts() {
  const query = state.search.trim().toLowerCase();
  return products.filter(product => {
    const matchesCategory = state.category === 'Alle' || product.category === state.category;
    const haystack = `${product.sku} ${product.name} ${product.manufacturer} ${product.unit}`.toLowerCase();
    return matchesCategory && (!query || haystack.includes(query));
  });
}

function renderCatalog() {
  const list = filteredProducts();
  els.resultCount.textContent = `${list.length} Treffer`;
  els.catalogBody.innerHTML = list.map(product => `
    <tr>
      <td>
        <span class="article-title">${escapeHtml(product.name)}</span>
        <span class="article-meta">${escapeHtml(product.sku)} · ${escapeHtml(product.unit)}</span>
      </td>
      <td><span class="pill">${escapeHtml(product.category)}</span></td>
      <td class="num">${euro.format(product.price)}</td>
      <td class="num"><button class="icon-btn" data-add="${product.id}" title="Hinzufügen">+</button></td>
    </tr>
  `).join('');
}

function renderCart() {
  els.emptyCart.hidden = state.cart.length > 0;
  els.lineCount.textContent = state.cart.length;
  els.cartBody.innerHTML = state.cart.map(line => {
    const product = getProduct(line.productId);
    if (!product) return '';
    return `
      <tr>
        <td>
          <span class="article-title">${escapeHtml(product.name)}</span>
          <span class="article-meta">${escapeHtml(product.sku)} · ${euro.format(product.price)} / ${escapeHtml(product.unit)}</span>
        </td>
        <td class="num"><input class="cart-input" type="number" min="0" step="1" value="${line.qty}" data-field="qty" data-line="${line.id}"></td>
        <td class="num"><input class="cart-input" type="number" min="1" step="1" value="${line.months}" data-field="months" data-line="${line.id}" ${isRecurring(product) ? '' : 'disabled'}></td>
        <td class="num"><input class="cart-input" type="number" min="0" max="100" step="0.5" value="${line.discount}" data-field="discount" data-line="${line.id}"></td>
        <td class="num"><strong>${getDisplayTotal(getLineTotal(product, line))}</strong></td>
        <td class="num"><button class="icon-btn remove-btn" data-remove="${line.id}" title="Entfernen">x</button></td>
      </tr>
    `;
  }).join('');
  renderTotals();
}

function renderTotals() {
  const totals = state.cart.reduce((acc, line) => {
    const product = getProduct(line.productId);
    if (!product) return acc;
    const lineTotal = getLineTotal(product, line);
    acc.grand += lineTotal;
    acc.weight += product.weight * (Number(line.qty) || 0);
    if (product.category === 'Software') acc.software += lineTotal;
    else if (product.category === 'Dienstleistung') acc.service += lineTotal;
    else acc.hardware += lineTotal;
    return acc;
  }, { hardware: 0, software: 0, service: 0, grand: 0, weight: 0 });

  els.hardwareTotal.textContent = getDisplayTotal(totals.hardware);
  els.softwareTotal.textContent = getDisplayTotal(totals.software);
  els.serviceTotal.textContent = getDisplayTotal(totals.service);
  els.grandTotal.textContent = getDisplayTotal(totals.grand);
  els.weightTotal.textContent = `${amount.format(totals.weight)} g`;
}

function addProduct(productId, qty = 1, months) {
  const product = getProduct(productId);
  if (!product) return;
  const existing = state.cart.find(line => line.productId === productId && Number(line.discount) === 0);
  if (existing) {
    existing.qty += qty;
  } else {
    state.cart.push({
      id: crypto.randomUUID ? crypto.randomUUID() : `${Date.now()}-${Math.random()}`,
      productId,
      qty,
      months: months || (isRecurring(product) ? 12 : 1),
      discount: 0
    });
  }
  saveCart();
  renderCart();
}

function addStarterPackage() {
  addProduct(products.find(product => product.sku === 'Gateway LRW Indoor RAK 7268V2')?.id, 1);
  addProduct(products.find(product => product.sku === 'NOVOS 3 Temp_rH weiß LRW')?.id, 10);
  addProduct(products.find(product => product.sku === 'Sensise Plattform - 36 Monate Mindestvertragslaufzeit')?.id, 10, 36);
  addProduct(products.find(product => product.sku === 'Projektsetup: Erweitert')?.id, 10);
}

function exportCsv() {
  const rows = [
    ['Projekt', els.projectName.value],
    ['Kunde', els.customerName.value],
    [],
    ['Artikelgruppe', 'Kurzbezeichnung', 'Bezeichnung', 'Einheit', 'Einzelpreis', 'Menge', 'Monate', 'Rabatt %', 'Summe netto']
  ];

  state.cart.forEach(line => {
    const product = getProduct(line.productId);
    if (!product) return;
    rows.push([
      product.category,
      product.sku,
      product.name,
      product.unit,
      product.price.toFixed(2),
      line.qty,
      isRecurring(product) ? line.months : '',
      line.discount,
      getLineTotal(product, line).toFixed(2)
    ]);
  });

  const csv = rows.map(row => row.map(cell => `"${String(cell ?? '').replaceAll('"', '""')}"`).join(';')).join('\n');
  const blob = new Blob([csv], { type: 'text/csv;charset=utf-8' });
  const link = document.createElement('a');
  link.href = URL.createObjectURL(blob);
  link.download = `${sanitizeFilename(els.projectName.value || 'sensise-kalkulation')}.csv`;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(link.href);
}

function sanitizeFilename(value) {
  return value.trim().replace(/[\\/:*?"<>|]+/g, '-').replace(/\s+/g, '_') || 'sensise-kalkulation';
}

function escapeHtml(value) {
  return String(value).replace(/[&<>"']/g, char => ({
    '&': '&amp;',
    '<': '&lt;',
    '>': '&gt;',
    '"': '&quot;',
    "'": '&#039;'
  })[char]);
}

function init() {
  const categories = ['Alle', ...new Set(products.map(product => product.category))];
  els.categoryFilter.innerHTML = categories.map(category => `<option>${escapeHtml(category)}</option>`).join('');
  els.catalogCount.textContent = products.length;

  els.search.addEventListener('input', event => {
    state.search = event.target.value;
    renderCatalog();
  });
  els.categoryFilter.addEventListener('change', event => {
    state.category = event.target.value;
    renderCatalog();
  });
  els.catalogBody.addEventListener('click', event => {
    const button = event.target.closest('[data-add]');
    if (button) addProduct(button.dataset.add);
  });
  els.cartBody.addEventListener('input', event => {
    const input = event.target.closest('[data-field]');
    if (!input) return;
    const line = state.cart.find(item => item.id === input.dataset.line);
    if (!line) return;
    line[input.dataset.field] = Number(input.value) || 0;
    saveCart();
    renderCart();
  });
  els.cartBody.addEventListener('click', event => {
    const button = event.target.closest('[data-remove]');
    if (!button) return;
    state.cart = state.cart.filter(line => line.id !== button.dataset.remove);
    saveCart();
    renderCart();
  });
  els.vatMode.addEventListener('change', renderCart);
  document.getElementById('starter-btn').addEventListener('click', addStarterPackage);
  document.getElementById('clear-btn').addEventListener('click', () => {
    if (!state.cart.length || confirm('Kalkulation wirklich leeren?')) {
      state.cart = [];
      saveCart();
      renderCart();
    }
  });
  document.getElementById('csv-btn').addEventListener('click', exportCsv);
  document.getElementById('print-btn').addEventListener('click', () => window.print());

  renderCatalog();
  renderCart();
}

init();
